-- noinspection SqlNoDataSourceInspectionForFile
DROP TABLE IF EXISTS morador;
DROP TABLE IF EXISTS categoria;

CREATE TABLE morador
(
    id          BIGINT PRIMARY KEY AUTO_INCREMENT,
    nome        VARCHAR(50) NOT NULL CHECK (LENGTH(nome) >= 5),
    email       VARCHAR(50) NOT NULL CHECK (LENGTH(email) >= 10),
    telefone    VARCHAR(20) NOT NULL CHECK (LENGTH(telefone) >= 11),
    apartamento BIGINT      NOT NULL UNIQUE CHECK (apartamento > 0),
    tipo        VARCHAR(20) NOT NULL
);

CREATE TABLE categoria
(
    id         BIGINT PRIMARY KEY AUTO_INCREMENT,
    nome       VARCHAR(50) NOT NULL CHECK (LENGTH(nome) >= 5),
    descricao VARCHAR(100) NOT NULL CHECK (LENGTH(descricao) >= 10),
    habilitada BIGINT  NOT NULL CHECK ( habilitada < 2 )
);


